#include "ft_printf.h"

void	ft_bzero(void *s, size_t len)
{
	size_t			i;
	unsigned char	*r;

	i = 0;
	r = (unsigned char *)s;
	while (i < len)
	{
		r[i] = 0;
		i++;
	}
}
